async function main() {
  const [recipeCat, insumosCat, users, products, existingRecipes] = await Promise.all([
    prisma.category.findFirst({ where: { slug: 'tipo-receta' }, select: { id: true } }),
    prisma.category.findFirst({ where: { slug: 'tipo-insumos' }, select: { id: true } }),
    prisma.user.findMany({ select: { id: true }, orderBy: { createdAt: 'asc' }, take: 1 }),
    prisma.product.findMany({ select: { id: true, name: true, sku: true, unit: true, categoryId: true } }),
    prisma.recipe.findMany({ where: { isActive: true }, select: { id: true, name: true, createdById: true } }),
  ]);

  if (!recipeCat || !insumosCat) throw new Error('Faltan categorías tipo-receta o tipo-insumos.');
  if (!users.length) throw new Error('No hay usuarios.');

  const recipeCategoryId = recipeCat.id;
  const insumosCategoryId = insumosCat.id;
  const userId = users[0].id;
  const productByKey = new Map(products.map((p) => [normalizeKey(p.name), p]));
  const usedSkus = new Set(products.map((p) => p.sku));
  const recipesByKey = new Map(existingRecipes.map((r) => [normalizeKey(r.name), r]));

  function sku(name: string): string {
    const base = normalizeKey(name).replace(/[^A-Z0-9]+/g, '-').replace(/-+/g, '-').replace(/^-|-$/g, '') || 'RECETA';
    let candidate = base;
    let n = 1;
    while (usedSkus.has(candidate)) { candidate = `${base}-${n}`; n += 1; }
    usedSkus.add(candidate);
    return candidate;
  }

  async function ensureProduct(name: string, isRecipe: boolean, unit: string) {
    const key = normalizeKey(name);
    const existing = productByKey.get(key);
    if (existing) return existing;
    const created = await prisma.product.create({
      data: {
        name: name.trim(),
        sku: sku(name),
        categoryId: isRecipe ? recipeCategoryId : insumosCategoryId,
        unit,
        isSellable: isRecipe,
        isIngredient: true,
        isProduced: isRecipe,
        isActive: true,
        avgCost: 0,
        lastCost: 0,
        salePrice: 0,
      },
    });
    productByKey.set(key, created);
    return created;
  }

  let created = 0;
  let updated = 0;

  for (const def of RECIPES) {
    const recipeProduct = await ensureProduct(def.name, true, def.yieldUnit === 'kg' ? 'kg' : 'unidad');
    const existing = recipesByKey.get(normalizeKey(def.name));

    const ingredientRows: Array<{ productId: string; qtyPerYield: number; unit: string; sortOrder: number }> = [];
    for (let i = 0; i < def.ingredients.length; i++) {
      const ing = def.ingredients[i];
      const product = await ensureProduct(ing.productName, ing.isRecipe ?? false, ing.unit);
      ingredientRows.push({ productId: product.id, qtyPerYield: ing.qty, unit: ing.unit, sortOrder: i });
    }

    const baseData = {
      name: def.name,
      description: 'Según planillas (Cordon Blue, Bruschettas, Petit Beef, Gyoza).',
      category: null as string | null,
      yieldQty: def.yieldQty,
      yieldUnit: def.yieldUnit,
      productId: recipeProduct.id,
      isActive: true,
      createdById: existing?.createdById ?? userId,
    };

    if (existing) {
      await prisma.recipe.update({
        where: { id: existing.id },
        data: { ...baseData, ingredients: { deleteMany: {}, create: ingredientRows } },
      });
      updated++;
      console.log('Actualizada:', def.name);
    } else {
      await prisma.recipe.create({
        data: { ...baseData, createdById: userId, ingredients: { create: ingredientRows } },
      });
      created++;
      console.log('Creada:', def.name);
    }
  }

  console.log('---');
  console.log('Recetas creadas:', created, '| actualizadas:', updated);
}

main()
